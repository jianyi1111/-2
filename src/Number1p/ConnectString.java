package Number1p;

public class ConnectString {
	public static void main(String args[]) {
		String a="他叫李狗蛋";
		String b="今年19岁";
		String c=a+","+b;
		String d="我来做一个介绍";
		d+=c;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}
}
